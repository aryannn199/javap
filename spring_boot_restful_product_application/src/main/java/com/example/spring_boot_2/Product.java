package com.example.spring_boot_2;

public class Product {
	private String id;
	private String name;
	
	// Constructor with Filds 
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
